// For Loop
// Help you to repeat a block of code.

// Increment Operator
// ++ -> increase the value 1
// Pre -   ()
let a = 10;
let b = ++a; //(++a ->  10+1)
console.log(a); // 11
console.log(b); // 11

/* Exp and Result Table
   Line No | a          | Result b
     1     |  10        |  NA
     2     |  11        |  11
     3     | 11 - print | 11
     4     | 11         | 11 - print  */

let c = 10;
let d = c++;
console.log(c); // 11
console.log(d); // 10

